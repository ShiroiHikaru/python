# 연산자는 자바스크립트랑 똑같아요. 그거야 사칙연산이니까?
# 문자열과 스트링 선언 역시 자바스크립트랑 똑같아요.
# 결과값 확인은 디버깅으로 확인해요.

print(5 + 6); # 더하기 연산자, 11

print(400 - 245); # 빼기 연산자, 155

print(563 * 4); # 곱하기 연산자, 2252

print(2 ** 56); # 거듭제곱 연산자, 72057594037927936

print(400 / 45); # 나눗셈 연산자, 8.88888888888889

print(90 % 4); # 나머지 연산자, 2